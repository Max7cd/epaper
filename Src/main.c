void DEV_InitMode(int mode) {
	free(BlackImage);
	BlackImage = (UBYTE *)malloc((Imagesize*(mode+1)));
	displaymode = mode;

	if(mode == MODE_BW) {
		EPD_2IN7_Init();
	} else {
		EPD_2IN7_Init_4Gray();
	}

	EPD_2IN7_Clear();
	DEV_Delay_ms(1000);

	HAL_UART_Receive_DMA(&huart3, BlackImage, (Imagesize*(displaymode+1)));
}

void DEV_DisplayImage(void) {
	if(displaymode == MODE_BW) {
		EPD_2IN7_Display(BlackImage);
	} else {
		EPD_2IN7_4GrayDisplay(BlackImage);
	}
	DEV_Delay_ms(1000);
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if (huart->Instance == USART3)
	{
		HAL_ResumeTick();
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);

		DEV_DisplayImage();

		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);

		HAL_UART_Receive_DMA(&huart3, BlackImage, (Imagesize*(displaymode+1)));
	}
}


void HAL_RTC_AlarmAEventCallback(RTC_HandleTypeDef *hrtc)
{
	HAL_ResumeTick();
	HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);

	// Display should be reset at least once a day to stop "burn-in"
	EPD_2IN7_Clear();
	DEV_Delay_ms(1000);

	DEV_DisplayImage();

	HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	HAL_ResumeTick();
    if (GPIO_Pin == GPIO_PIN_13) { // User Button on the STM32
    	HAL_GPIO_TogglePin(LD1_GPIO_Port, LD1_Pin);
    	HAL_UART_DMAStop(&huart3);
    	DEV_InitMode(HAL_GPIO_ReadPin(LD1_GPIO_Port, LD1_Pin));
    }
    if (GPIO_Pin == GPIO_PIN_10) { // Button 1 on the display
    	CDC_Transmit_FS((uint8_t*)"Pressed Button 1\r\n", 18);
    }
    if (GPIO_Pin == GPIO_PIN_12) { // Button 2 on the display
    	CDC_Transmit_FS((uint8_t*)"Pressed Button 2\r\n", 18);
    }
    if (GPIO_Pin == GPIO_PIN_14) { // Button 3 on the display
    	CDC_Transmit_FS((uint8_t*)"Pressed Button 3\r\n", 18);
    }
    if (GPIO_Pin == GPIO_PIN_15) { // Button 4 on the display
    	CDC_Transmit_FS((uint8_t*)"Pressed Button 4\r\n", 18);
    }
    HAL_Delay(300); // Debounce
}
